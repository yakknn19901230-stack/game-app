function drawEnemies() {
  for (const e of enemies) {
    if (!e.alive && e.squashTimer <= 0) continue;
    const squash = e.alive ? 1 : 0.3;
    const h = e.h * squash;
    const y = e.y + e.h - h;
    const wobble = e.alive ? Math.sin(frameCount * 0.2) * 2 : 0;

    // 体
    ctx.fillStyle = "#a0522d";
    ctx.beginPath();
    ctx.ellipse(e.x + e.w / 2, y + h * 0.45, e.w / 2 + wobble * 0.3, h * 0.45, 0, 0, Math.PI * 2);
    ctx.fill();
    // 足
    ctx.fillStyle = "#5c3317";
    ctx.beginPath();
    ctx.ellipse(e.x + e.w * 0.28 + wobble, y + h - 5, 9, 6, 0, 0, Math.PI * 2);
    ctx.ellipse(e.x + e.w * 0.72 - wobble, y + h - 5, 9, 6, 0, 0, Math.PI * 2);
    ctx.fill();
    if (e.alive) {
      // 目
      ctx.fillStyle = "#fff";
      ctx.beginPath();
      ctx.ellipse(e.x + e.w * 0.35, y + h * 0.35, 6, 8, 0, 0, Math.PI * 2);
      ctx.ellipse(e.x + e.w * 0.65, y + h * 0.35, 6, 8, 0, 0, Math.PI * 2);
      ctx.fill();
      ctx.fillStyle = "#000";
      const look = e.vx > 0 ? 2 : -2;
      ctx.beginPath();
      ctx.arc(e.x + e.w * 0.35 + look, y + h * 0.38, 3, 0, Math.PI * 2);
      ctx.arc(e.x + e.w * 0.65 + look, y + h * 0.38, 3, 0, Math.PI * 2);
      ctx.fill();
      // 眉
      ctx.strokeStyle = "#3a2010";
      ctx.lineWidth = 3;
      ctx.beginPath();
      ctx.moveTo(e.x + e.w * 0.25, y + h * 0.18);
      ctx.lineTo(e.x + e.w * 0.44, y + h * 0.26);
      ctx.moveTo(e.x + e.w * 0.75, y + h * 0.18);
      ctx.lineTo(e.x + e.w * 0.56, y + h * 0.26);
      ctx.stroke();
    }
  }
}

function drawPlayer() {
  const p = player;
  const cx = p.x + p.w / 2;
  const feetY = p.y + p.h;
  const dying = gameState === STATE.DYING;
  const blink = dying && Math.floor(p.deadTimer / 4) % 2 === 0;
  if (blink) return;
  // ダメージ後の無敵時間中は点滅させる
  if (p.invuln > 0 && Math.floor(frameCount / 4) % 2 === 0) return;

  // 当たり判定はそのままに、見た目だけ足元を基準に拡大して描く
  // キノコを取って大きいときはさらに大きく
  const S = p.big ? 1.8 : 1.35;
  // スター無敵中は虹色に(canvasのfilterはiOSで効かないため色を直接変える)
  // 終わる直前(3秒切ったら)は点滅して知らせる
  const rainbow = p.star > 0 && (p.star > 180 || Math.floor(frameCount / 6) % 2 === 0);
  const hue = (frameCount * 14) % 360;
  // ファイア状態は服がオレンジになる
  const suit = rainbow ? `hsl(${hue}, 95%, 55%)` : p.fire ? "#ff8c00" : "#e63946";
  const overallColor = rainbow ? `hsl(${(hue + 120) % 360}, 95%, 55%)` : "#3563c4";
  const legColor = rainbow ? `hsl(${(hue + 240) % 360}, 90%, 45%)` : "#2b50a1";

  // 虹色のオーラを背中側に描く
  if (rainbow) {
    const auraCx = cx, auraCy = feetY - (p.h / 2) * S;
    const auraR = 46 * S;
    const grad = ctx.createRadialGradient(auraCx, auraCy, 6, auraCx, auraCy, auraR);
    grad.addColorStop(0, `hsla(${hue}, 100%, 70%, 0.6)`);
    grad.addColorStop(1, "hsla(0, 0%, 100%, 0)");
    ctx.fillStyle = grad;
    ctx.beginPath();
    ctx.arc(auraCx, auraCy, auraR, 0, Math.PI * 2);
    ctx.fill();
  }

  // なかまに乗っているときは恐竜を先に描いて、体を少し上にずらす
  const ride = p.buddy ? 22 : 0;
  if (p.buddy) {
    const runPhase = Math.abs(p.vx) > 0.3 && p.onGround ? p.runFrame * 2 : 0;
    drawDino(cx + p.facing * 8, feetY, p.facing, S * 0.95, runPhase);
  }

  ctx.save();
  ctx.translate(cx, feetY);
  ctx.scale(S, S);
  ctx.translate(0, -p.h - ride);
  if (p.facing < 0) ctx.scale(-1, 1);

  const legSwing = p.onGround && Math.abs(p.vx) > 0.3 ? Math.sin(p.runFrame * 2) * 7 : 0;
  const jumping = !p.onGround;

  // 足
  ctx.strokeStyle = legColor;
  ctx.lineWidth = 8;
  ctx.lineCap = "round";
  ctx.beginPath();
  ctx.moveTo(-6, p.h - 18);
  ctx.lineTo(-6 + (jumping ? -4 : legSwing), p.h - 4);
  ctx.moveTo(6, p.h - 18);
  ctx.lineTo(6 + (jumping ? 4 : -legSwing), p.h - 4);
  ctx.stroke();

  // 体(オーバーオール)
  ctx.fillStyle = overallColor;
  ctx.beginPath();
  ctx.roundRect(-11, p.h - 30, 22, 16, 5);
  ctx.fill();
  // シャツ
  ctx.fillStyle = suit;
  ctx.beginPath();
  ctx.roundRect(-12, p.h - 36, 24, 10, 4);
  ctx.fill();

  // 腕
  ctx.strokeStyle = suit;
  ctx.lineWidth = 6;
  ctx.beginPath();
  if (jumping) {
    ctx.moveTo(-11, p.h - 33); ctx.lineTo(-18, p.h - 42);
    ctx.moveTo(11, p.h - 33); ctx.lineTo(18, p.h - 42);
  } else {
    ctx.moveTo(-11, p.h - 33); ctx.lineTo(-15 - legSwing * 0.5, p.h - 24);
    ctx.moveTo(11, p.h - 33); ctx.lineTo(15 + legSwing * 0.5, p.h - 24);
  }
  ctx.stroke();

  ctx.restore();

  // 頭(顔) - 反転させずに描く(顔写真が裏返ると変なので)
  // 顔がゲームの主役なので大きめに。11 = 拡大前の頭中心の足元からの高さ(p.h - (15 - 4))
  const headR = 15 * S;
  const headY = feetY - (p.h - 11 + ride) * S;
  drawFaceCircle(ctx, cx, headY, headR);

  // スター無敵中は顔にもうっすら虹色をのせる
  if (rainbow) {
    ctx.fillStyle = `hsla(${hue}, 100%, 60%, 0.3)`;
    ctx.beginPath();
    ctx.arc(cx, headY, headR, 0, Math.PI * 2);
    ctx.fill();
  }

  // 帽子: 顔が隠れないよう、頭のてっぺんに乗る細いキャップにする
  ctx.save();
  ctx.translate(cx, headY);
  if (p.facing < 0) ctx.scale(-1, 1);
  ctx.fillStyle = suit;
  ctx.beginPath();
  ctx.arc(0, 0, headR + 2, Math.PI + 0.45, -0.45);
  ctx.closePath();
  ctx.fill();
  // つば
  ctx.beginPath();
  ctx.roundRect(headR * 0.45, -headR * 0.85, headR * 1.05, headR * 0.3, 4);
  ctx.fill();
  ctx.restore();
}

function drawParticles() {
  for (const p of particles) {
    ctx.globalAlpha = Math.min(1, p.life / 20);
    ctx.fillStyle = p.color;
    ctx.fillRect(p.x - p.size / 2, p.y - p.size / 2, p.size, p.size);
  }
  ctx.globalAlpha = 1;

  for (const p of popups) {
    ctx.globalAlpha = Math.min(1, p.life / 15);
    if (p.type === "coin") {
      ctx.fillStyle = "#ffd700";
      ctx.beginPath();
      ctx.arc(p.x, p.y, 12, 0, Math.PI * 2);
      ctx.fill();
      ctx.strokeStyle = "#b8860b";
      ctx.lineWidth = 3;
      ctx.stroke();
    } else {
      ctx.fillStyle = "#fff";
      ctx.font = "bold 20px sans-serif";
      ctx.textAlign = "center";
      ctx.fillText(p.text, p.x, p.y);
    }
  }
  ctx.globalAlpha = 1;
}

function drawHUD() {
  if (gameState === STATE.TITLE) return;
  ctx.save();
  ctx.fillStyle = "rgba(0,0,0,0.35)";
  ctx.beginPath();
  ctx.roundRect(14, 12, 540, 46, 12);
  ctx.fill();

  ctx.fillStyle = "#fff";
  ctx.font = "bold 22px sans-serif";
  ctx.textAlign = "left";
  ctx.textBaseline = "middle";

  // 残機(顔アイコン)
  drawFaceCircle(ctx, 44, 35, 16);
  ctx.fillStyle = "#fff";
  ctx.fillText(`×${lives}`, 66, 36);

  // コイン
  ctx.fillStyle = "#ffd700";
  ctx.beginPath();
  ctx.arc(140, 35, 11, 0, Math.PI * 2);
  ctx.fill();
  ctx.fillStyle = "#fff";
  ctx.fillText(`×${coinCount}`, 158, 36);

  // スコアとタイム
  ctx.fillText(`SCORE ${score}`, 230, 36);
  ctx.fillStyle = timeLeft <= 30 ? "#ff8080" : "#fff";
  ctx.fillText(`⏱ ${timeLeft}`, 380, 36);
  ctx.fillStyle = "#ffd166";
  ctx.fillText(`${MODES[currentMode].icon}${curLv().theme.icon} Lv${currentLevel + 1}`, 462, 36);
  ctx.restore();
}

