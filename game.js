// ---------- タイルユーティリティ ----------
function tileAt(tx, ty) {
  if (tx < 0 || tx >= MAP_COLS) return "#"; // 端は壁扱い
  if (ty < 0 || ty >= MAP_ROWS) return " ";
  return tiles[ty][tx];
}
function isSolidAt(tx, ty) { return SOLID.has(tileAt(tx, ty)); }

// AABBとタイルの衝突解決(軸ごと)。ヒットしたブロック情報を返す
function moveAndCollide(body) {
  const hits = { top: null };

  // X方向
  body.x += body.vx;
  let x0 = Math.floor(body.x / TILE), x1 = Math.floor((body.x + body.w - 1) / TILE);
  let y0 = Math.floor(body.y / TILE), y1 = Math.floor((body.y + body.h - 1) / TILE);
  for (let ty = y0; ty <= y1; ty++) {
    for (let tx = x0; tx <= x1; tx++) {
      if (!isSolidAt(tx, ty)) continue;
      if (body.vx > 0) body.x = tx * TILE - body.w;
      else if (body.vx < 0) body.x = (tx + 1) * TILE;
      body.hitWall = true;
      body.vx = 0;
    }
  }

  // Y方向
  body.y += body.vy;
  body.onGround = false;
  x0 = Math.floor(body.x / TILE); x1 = Math.floor((body.x + body.w - 1) / TILE);
  y0 = Math.floor(body.y / TILE); y1 = Math.floor((body.y + body.h - 1) / TILE);
  for (let ty = y0; ty <= y1; ty++) {
    for (let tx = x0; tx <= x1; tx++) {
      if (!isSolidAt(tx, ty)) continue;
      if (body.vy > 0) {
        body.y = ty * TILE - body.h;
        body.vy = 0;
        body.onGround = true;
      } else if (body.vy < 0) {
        body.y = (ty + 1) * TILE;
        body.vy = 0;
        if (!hits.top) hits.top = { tx, ty };
      }
    }
  }
  return hits;
}

// ---------- ブロックを下から叩いたとき ----------
function bumpBlock(tx, ty) {
  const ch = tileAt(tx, ty);
  if (ch === "M" || ch === "W" || ch === "T" || ch === "Y") {
    // アイテム入りブロック: M=キノコ W=フラワー T=スター Y=恐竜のなかま
    tiles[ty][tx] = "U";
    score += 200;
    sfx.sprout();
    const type = ch === "M" ? "mushroom" : ch === "W" ? "flower" : ch === "T" ? "star" : "buddy";
    items.push({
      x: tx * TILE + 4, y: ty * TILE - 40,
      w: 40, h: 40,
      vx: type === "mushroom" ? 1.5 : type === "star" ? 2.5 : type === "buddy" ? 1.2 : 0, // フラワーは動かない
      vy: 0, type, taken: false,
    });
  } else if (ch === "?") {
    tiles[ty][tx] = "U";
    coinCount++;
    score += 200;
    sfx.coin();
    popups.push({ x: tx * TILE + TILE / 2, y: ty * TILE, vy: -6, life: 30, type: "coin" });
  } else if (ch === "B") {
    tiles[ty][tx] = " ";
    score += 50;
    sfx.break();
    for (let i = 0; i < 8; i++) {
      particles.push({
        x: tx * TILE + TILE / 2, y: ty * TILE + TILE / 2,
        vx: (Math.random() - 0.5) * 8, vy: -Math.random() * 8 - 2,
        life: 40, color: "#b5651d", size: 7,
      });
    }
  } else if (SOLID.has(ch)) {
    sfx.bump();
  }
}

// ---------- プレイヤーのダメージ・死亡 ----------
// 大きいときは1回だけ耐えて小さくなる。小さいときはミス。
function damagePlayer() {
  if (player.invuln > 0 || player.star > 0) return;
  if (player.buddy) {
    // なかまがかばって逃げていく(また捕まえられるよう少しの間走る)
    player.buddy = false;
    player.invuln = 120;
    sfx.shrink();
    items.push({
      x: player.x, y: player.y, w: 40, h: 34,
      vx: 4 * (player.facing || 1), vy: -6,
      type: "flee", life: 90, taken: false,
    });
    return;
  }
  if (player.fire) {
    // ファイア → 大きいだけの状態に戻る
    player.fire = false;
    player.invuln = 120;
    sfx.shrink();
  } else if (player.big) {
    player.big = false;
    player.invuln = 120;
    sfx.shrink();
  } else {
    killPlayer();
  }
}

function killPlayer() {
  if (gameState !== STATE.PLAYING) return;
  gameState = STATE.DYING;
  player.deadTimer = 0;
  player.vy = -11;
  stopBgm();
  sfx.die();
}

function afterDeath() {
  lives--;
  if (lives <= 0) {
    gameState = STATE.GAMEOVER;
    document.getElementById("gameover-score").textContent = `スコア: ${score}　コイン: ${coinCount}`;
    document.getElementById("gameover-screen").classList.remove("hidden");
  } else {
    initLevel();
    gameState = STATE.PLAYING;
    startBgm();
  }
}

// ---------- 更新処理 ----------
function update() {
  frameCount++;

  if (gameState === STATE.DYING) {
    // 死亡演出: その場でぴょんと跳ねて落下
    player.deadTimer++;
    player.vy += GRAVITY;
    player.y += player.vy;
    if (player.deadTimer > 110) afterDeath();
    return;
  }

  if (gameState !== STATE.PLAYING) return;

  if (player.invuln > 0) player.invuln--;
  if (player.star > 0) {
    player.star--;
    if (player.star === 0) setBgmMode("normal"); // 無敵が終わったら曲を戻す
    // 無敵中はキラキラの粒をまき散らす
    if (frameCount % 4 === 0) {
      particles.push({
        x: player.x + Math.random() * player.w,
        y: player.y + Math.random() * player.h,
        vx: (Math.random() - 0.5) * 2.5,
        vy: -1 - Math.random() * 2,
        life: 28,
        color: `hsl(${Math.floor(Math.random() * 360)}, 90%, 65%)`,
        size: 6,
      });
    }
  }

  // ファイアボール発射
  if (firePressed) {
    firePressed = false;
    if (player.fire && fireballs.length < 2) {
      sfx.fire();
      fireballs.push({
        x: player.x + (player.facing > 0 ? player.w : -16),
        y: player.y + 12,
        w: 16, h: 16,
        vx: 7 * player.facing, vy: 2, life: 110,
      });
    }
  }

  // タイマー
  if (frameCount % 60 === 0) {
    timeLeft--;
    if (timeLeft <= 0) { killPlayer(); return; }
  }

  // --- プレイヤー操作 ---
  // なかまに乗っていると速く走れて高くジャンプできる
  const maxSpd = player.buddy ? MAX_SPEED * 1.3 : MAX_SPEED;
  if (keys.left)  { player.vx -= MOVE_ACCEL; player.facing = -1; }
  if (keys.right) { player.vx += MOVE_ACCEL; player.facing = 1; }
  if (!keys.left && !keys.right) player.vx *= FRICTION;
  player.vx = Math.max(-maxSpd, Math.min(maxSpd, player.vx));
  if (Math.abs(player.vx) < 0.05) player.vx = 0;

  // ジャンプ: 押した瞬間に反応させるため、先行入力とコヨーテタイムを持たせる。
  // 着地の少し前に押してもすぐ跳べて、足場から落ちた直後でも跳べる。
  if (jumpPressed) {
    player.jumpBuffer = 8;
    jumpPressed = false;
  }
  if (player.onGround) player.coyote = 7;
  else if (player.coyote > 0) player.coyote--;

  if (player.jumpBuffer > 0) {
    player.jumpBuffer--;
    if (player.coyote > 0) {
      player.vy = JUMP_POWER * (player.buddy ? 1.06 : 1);
      player.coyote = 0;
      player.jumpBuffer = 0;
      player.onGround = false;
      sfx.jump();
    }
  }
  // ジャンプボタンを離すと上昇が弱まる(可変ジャンプ)
  if (!keys.jump && player.vy < -4) player.vy = -4;

  player.vy += GRAVITY;
  if (player.vy > 16) player.vy = 16;

  const hits = moveAndCollide(player);
  if (hits.top) bumpBlock(hits.top.tx, hits.top.ty);

  if (Math.abs(player.vx) > 0.3 && player.onGround) player.runFrame += Math.abs(player.vx) * 0.06;

  // 穴に落ちた
  if (player.y > WORLD_H + 100) { killPlayer(); return; }

  // --- コイン取得 ---
  for (const c of coins) {
    if (c.taken) continue;
    if (Math.abs(player.x + player.w / 2 - c.x) < 34 && Math.abs(player.y + player.h / 2 - c.y) < 38) {
      c.taken = true;
      coinCount++;
      score += 100;
      sfx.coin();
      popups.push({ x: c.x, y: c.y, vy: -3, life: 25, type: "score", text: "100" });
    }
  }

  // --- 敵の更新 ---
  for (const e of enemies) {
    if (!e.alive) {
      if (e.squashTimer > 0) e.squashTimer--;
      continue;
    }
    // 画面からかなり遠い敵は動かさない
    if (Math.abs(e.x - player.x) > canvas.width * 1.2) continue;

    e.hitWall = false;
    const prevVx = e.vx; // moveAndCollideが壁でvxを0にするため、反転用に覚えておく
    e.vy += GRAVITY;
    if (e.vy > 16) e.vy = 16;
    moveAndCollide(e);
    if (e.hitWall) e.vx = -prevVx;

    // プレイヤーとの当たり判定
    if (rectsOverlap(player, e)) {
      if (player.star > 0) {
        // スターで無敵中は触れるだけで敵をたおせる
        e.alive = false;
        e.squashTimer = 30;
        score += 300;
        sfx.stomp();
        popups.push({ x: e.x + e.w / 2, y: e.y, vy: -3, life: 25, type: "score", text: "300" });
        continue;
      }
      const playerBottom = player.y + player.h;
      if (player.vy > 0 && playerBottom - e.y < 24) {
        // 踏みつけ
        e.alive = false;
        e.squashTimer = 30;
        player.vy = STOMP_BOUNCE;
        score += 300;
        sfx.stomp();
        popups.push({ x: e.x + e.w / 2, y: e.y, vy: -3, life: 25, type: "score", text: "300" });
      } else {
        damagePlayer();
        if (gameState !== STATE.PLAYING) return;
      }
    }
  }

  // --- アイテムの更新と取得 ---
  for (const it of items) {
    if (it.taken) continue;
    it.hitWall = false;
    const prevVx = it.vx;
    it.vy += GRAVITY;
    if (it.vy > 16) it.vy = 16;
    moveAndCollide(it);
    if (it.hitWall) it.vx = -prevVx;
    if (it.type === "star" && it.onGround) it.vy = -9; // スターは跳ねまわる
    if (it.y > WORLD_H + 100) { it.taken = true; continue; }
    if (it.type === "flee") {
      // 逃げていくなかま(取り直しはできない)
      if (it.life-- <= 0) it.taken = true;
      continue;
    }

    if (rectsOverlap(player, it)) {
      it.taken = true;
      score += 500;
      if (it.type === "star") {
        player.star = 1200; // 20秒間むてき!
        setBgmMode("star");
        sfx.star();
        popups.push({ x: it.x + it.w / 2, y: it.y, vy: -3, life: 35, type: "score", text: "むてき!" });
      } else if (it.type === "buddy") {
        player.buddy = true;
        sfx.buddy();
        popups.push({ x: it.x + it.w / 2, y: it.y, vy: -3, life: 40, type: "score", text: "なかまが きたよ!" });
      } else if (it.type === "flower") {
        player.big = true;
        player.fire = true;
        sfx.power();
        popups.push({ x: it.x + it.w / 2, y: it.y, vy: -3, life: 35, type: "score", text: "ファイア!" });
      } else {
        player.big = true;
        sfx.power();
        popups.push({ x: it.x + it.w / 2, y: it.y, vy: -3, life: 35, type: "score", text: "パワーアップ!" });
      }
    }
  }

  // --- ファイアボールの更新 ---
  for (const f of fireballs) {
    f.hitWall = false;
    f.vy += GRAVITY * 0.7;
    if (f.vy > 12) f.vy = 12;
    moveAndCollide(f);
    if (f.onGround) f.vy = -7; // 地面で跳ねる
    if (f.hitWall) f.life = 0;
    f.life--;
    if (f.y > WORLD_H + 50) f.life = 0;
    for (const e of enemies) {
      if (e.alive && f.life > 0 && rectsOverlap(f, e)) {
        e.alive = false;
        e.squashTimer = 30;
        f.life = 0;
        score += 200;
        sfx.stomp();
        popups.push({ x: e.x + e.w / 2, y: e.y, vy: -3, life: 25, type: "score", text: "200" });
      }
    }
  }
  fireballs = fireballs.filter(f => f.life > 0);

  // --- ゴール判定(旗の列に到達したらクリア) ---
  const ptx = Math.floor((player.x + player.w / 2) / TILE);
  if (flagCols.has(ptx)) {
    gameState = STATE.CLEAR;
    score += timeLeft * 10;
    stopBgm();
    sfx.clear();
    clearTimer = 0;
    setTimeout(() => {
      const isLast = currentLevel >= MODES[currentMode].levels.length - 1;
      document.getElementById("clear-title").textContent = isLast
        ? `${MODES[currentMode].name} ぜんぶクリア!🏆`
        : `レベル${currentLevel + 1} クリア!🎉`;
      document.getElementById("clear-score").textContent =
        `スコア: ${score}　コイン: ${coinCount}　残りタイム: ${timeLeft}`;
      document.getElementById("next-stage-btn").style.display = isLast ? "none" : "";
      document.getElementById("clear-screen").classList.remove("hidden");
    }, 1200);
  }

  // --- パーティクル・ポップアップ ---
  for (const p of particles) {
    p.vy += GRAVITY * 0.6;
    p.x += p.vx;
    p.y += p.vy;
    p.life--;
  }
  particles = particles.filter(p => p.life > 0);

  for (const p of popups) {
    p.y += p.vy;
    p.vy *= 0.92;
    p.life--;
  }
  popups = popups.filter(p => p.life > 0);

  // --- カメラ ---
  const targetX = player.x + player.w / 2 - canvas.width / 2;
  camera.x += (targetX - camera.x) * 0.12;
  camera.x = Math.max(0, Math.min(WORLD_W - canvas.width, camera.x));
  camera.y = Math.max(0, WORLD_H - canvas.height);
}

function rectsOverlap(a, b) {
  return a.x < b.x + b.w && a.x + a.w > b.x && a.y < b.y + b.h && a.y + a.h > b.y;
}

// ---------- 描画処理 ----------
function draw() {
  const W = canvas.width, H = canvas.height;
  const theme = curLv().theme;

  // 空(ステージごとの色)
  const sky = ctx.createLinearGradient(0, 0, 0, H);
  sky.addColorStop(0, theme.sky[0]);
  sky.addColorStop(1, theme.sky[1]);
  ctx.fillStyle = sky;
  ctx.fillRect(0, 0, W, H);

  // 遠景の雲と山(パララックス)
  drawBackground();

  ctx.save();
  ctx.translate(-Math.round(camera.x), -Math.round(camera.y));

  drawTiles();
  drawCoins();
  drawItems();
  drawEnemies();
  drawFireballs();
  if (gameState !== STATE.TITLE) drawPlayer();
  drawParticles();

  ctx.restore();

  drawHUD();

  // 🔥ボタンはファイア状態のときだけ表示する
  document.getElementById("touch-fire").classList.toggle(
    "hidden-btn",
    !(gameState === STATE.PLAYING && player && player.fire)
  );

  if (gameState === STATE.CLEAR) {
    clearTimer++;
    ctx.fillStyle = `rgba(255, 255, 255, ${Math.min(0.25, clearTimer / 200)})`;
    ctx.fillRect(0, 0, W, H);
    // その場ですぐ「ゴール!」と分かるように大きく表示する
    ctx.save();
    ctx.textAlign = "center";
    ctx.textBaseline = "middle";
    ctx.font = "bold 72px sans-serif";
    const bounce = Math.sin(clearTimer * 0.1) * 6;
    ctx.fillStyle = "rgba(0,0,0,0.4)";
    ctx.fillText("🚩 ゴール!", W / 2 + 4, H / 2 - 40 + bounce + 4);
    ctx.fillStyle = "#ffd166";
    ctx.fillText("🚩 ゴール!", W / 2, H / 2 - 40 + bounce);
    ctx.restore();
  }
}

function drawBackground() {
  const W = canvas.width, H = canvas.height;
  const theme = curLv().theme;

  // ステージごとの背景演出
  if (theme.deco === "sun") {
    ctx.fillStyle = "#fff3b0";
    ctx.beginPath();
    ctx.arc(W - 130, 100, 44, 0, Math.PI * 2);
    ctx.fill();
    ctx.strokeStyle = "rgba(255,243,176,0.6)";
    ctx.lineWidth = 5;
    for (let i = 0; i < 8; i++) {
      const a = (i * Math.PI) / 4 + frameCount * 0.003;
      ctx.beginPath();
      ctx.moveTo(W - 130 + Math.cos(a) * 56, 100 + Math.sin(a) * 56);
      ctx.lineTo(W - 130 + Math.cos(a) * 72, 100 + Math.sin(a) * 72);
      ctx.stroke();
    }
  } else if (theme.deco === "sunset") {
    ctx.fillStyle = "#ff7847";
    ctx.beginPath();
    ctx.arc(W / 2, H - 240, 70, 0, Math.PI * 2);
    ctx.fill();
    ctx.fillStyle = "rgba(255,120,71,0.25)";
    ctx.beginPath();
    ctx.arc(W / 2, H - 240, 110, 0, Math.PI * 2);
    ctx.fill();
  } else if (theme.deco === "volcano") {
    // 星と月
    ctx.fillStyle = "#fff";
    for (let i = 0; i < 40; i++) {
      const sx = ((i * 271) % 960 + camera.x * -0.05) % 960;
      const sy = (i * 137) % 300;
      const tw = 0.5 + Math.abs(Math.sin(frameCount * 0.03 + i)) * 1.6;
      ctx.fillRect((sx + 960) % 960, sy, tw, tw);
    }
    ctx.fillStyle = "#f6e7b0";
    ctx.beginPath();
    ctx.arc(W - 140, 90, 34, 0, Math.PI * 2);
    ctx.fill();
    ctx.fillStyle = theme.sky[0];
    ctx.beginPath();
    ctx.arc(W - 152, 80, 30, 0, Math.PI * 2);
    ctx.fill();
  }

  // 山(2層のパララックス)
  const mOff = -(camera.x * 0.15) % 700;
  ctx.fillStyle = theme.hillFar;
  for (let i = -1; i < W / 700 + 2; i++) {
    const bx = mOff + i * 700 + 260;
    ctx.beginPath();
    ctx.moveTo(bx, H);
    ctx.quadraticCurveTo(bx + 210, H - 640, bx + 420, H);
    ctx.fill();
  }
  const mOff2 = -(camera.x * 0.25) % 700;
  for (let i = -1; i < W / 700 + 2; i++) {
    const bx = mOff2 + i * 700;
    ctx.fillStyle = theme.hillNear;
    ctx.beginPath();
    ctx.moveTo(bx, H);
    ctx.quadraticCurveTo(bx + 175, H - 560, bx + 350, H);
    ctx.fill();
    if (theme.deco === "volcano") {
      // 山の頂上に溶岩の光
      ctx.fillStyle = "#ff6b35";
      ctx.beginPath();
      ctx.arc(bx + 175, H - 272, 14 + Math.sin(frameCount * 0.05 + i) * 3, 0, Math.PI * 2);
      ctx.fill();
    }
  }

  // 海(ぼうけんステージ): 山のふもとに波
  if (theme.deco === "sea") {
    ctx.fillStyle = "#1e6bb8";
    ctx.fillRect(0, H - 200, W, 80);
    ctx.fillStyle = "rgba(255,255,255,0.35)";
    const wOff = -(camera.x * 0.3) % 80;
    for (let i = -1; i < W / 80 + 2; i++) {
      const bx = wOff + i * 80;
      ctx.beginPath();
      ctx.arc(bx, H - 200 + Math.sin(frameCount * 0.05 + i) * 3, 10, 0, Math.PI, true);
      ctx.fill();
    }
  }

  // 雲(火山ステージはうっすら)
  ctx.fillStyle = theme.deco === "volcano" ? "rgba(120,90,110,0.5)" : "rgba(255,255,255,0.9)";
  const cOff = -(camera.x * 0.4) % 500;
  for (let i = -1; i < W / 500 + 2; i++) {
    const bx = cOff + i * 500;
    drawCloud(bx + 80, 80, 1);
    drawCloud(bx + 320, 150, 0.7);
  }
}

function drawCloud(x, y, s) {
  ctx.beginPath();
  ctx.arc(x, y, 28 * s, 0, Math.PI * 2);
  ctx.arc(x + 30 * s, y - 12 * s, 24 * s, 0, Math.PI * 2);
  ctx.arc(x + 60 * s, y, 26 * s, 0, Math.PI * 2);
  ctx.fill();
}

function drawTiles() {
  const startX = Math.max(0, Math.floor(camera.x / TILE) - 1);
  const endX = Math.min(MAP_COLS - 1, Math.ceil((camera.x + canvas.width) / TILE) + 1);

  for (let ty = 0; ty < MAP_ROWS; ty++) {
    for (let tx = startX; tx <= endX; tx++) {
      const ch = tiles[ty][tx];
      if (ch === " ") continue;
      const x = tx * TILE, y = ty * TILE;

      switch (ch) {
        case "#": {
          const theme = curLv().theme;
          const isTop = tileAt(tx, ty - 1) !== "#";
          ctx.fillStyle = theme.dirt;
          ctx.fillRect(x, y, TILE, TILE);
          ctx.fillStyle = "rgba(0,0,0,0.12)";
          ctx.fillRect(x + 4, y + 10, 14, 10);
          ctx.fillRect(x + 26, y + 28, 14, 10);
          if (isTop) {
            ctx.fillStyle = theme.grass;
            ctx.fillRect(x, y, TILE, 14);
            ctx.fillStyle = theme.grassHi;
            ctx.fillRect(x, y, TILE, 6);
            // 花畑ステージは草の上にお花を咲かせる
            if (theme.flowers && tx % 5 === 2) {
              const fc = theme.flowers[(tx * 7) % theme.flowers.length];
              ctx.fillStyle = fc;
              for (let i = 0; i < 4; i++) {
                const a = (i * Math.PI) / 2;
                ctx.beginPath();
                ctx.arc(x + 24 + Math.cos(a) * 5, y - 8 + Math.sin(a) * 5, 4, 0, Math.PI * 2);
                ctx.fill();
              }
              ctx.fillStyle = "#ffd166";
              ctx.beginPath();
              ctx.arc(x + 24, y - 8, 3.5, 0, Math.PI * 2);
              ctx.fill();
            }
            // 火山ステージは地面がところどころ光る
            if (theme.deco === "volcano" && tx % 7 === 3) {
              ctx.fillStyle = `rgba(255,107,53,${0.5 + Math.sin(frameCount * 0.06 + tx) * 0.3})`;
              ctx.fillRect(x + 14, y + 2, 20, 6);
            }
          }
          break;
        }
        case "B": {
          ctx.fillStyle = "#c8642e";
          ctx.fillRect(x, y, TILE, TILE);
          ctx.strokeStyle = "#7d3714";
          ctx.lineWidth = 3;
          ctx.strokeRect(x + 1.5, y + 1.5, TILE - 3, TILE - 3);
          ctx.beginPath();
          ctx.moveTo(x, y + TILE / 2); ctx.lineTo(x + TILE, y + TILE / 2);
          ctx.moveTo(x + TILE / 2, y); ctx.lineTo(x + TILE / 2, y + TILE / 2);
          ctx.moveTo(x + TILE / 4, y + TILE / 2); ctx.lineTo(x + TILE / 4, y + TILE);
          ctx.moveTo(x + TILE * 3 / 4, y + TILE / 2); ctx.lineTo(x + TILE * 3 / 4, y + TILE);
          ctx.stroke();
          break;
        }
        case "?":
        case "M":
        case "W":
        case "T":
        case "Y": {
          // アイテム入りブロックはどれも?ブロックの見た目(中身はお楽しみ)
          const bounce = Math.sin(frameCount * 0.1) * 2;
          ctx.fillStyle = "#f6a623";
          ctx.fillRect(x, y, TILE, TILE);
          ctx.strokeStyle = "#a86b00";
          ctx.lineWidth = 3;
          ctx.strokeRect(x + 1.5, y + 1.5, TILE - 3, TILE - 3);
          ctx.fillStyle = "#fff";
          ctx.font = "bold 28px sans-serif";
          ctx.textAlign = "center";
          ctx.textBaseline = "middle";
          ctx.fillText("?", x + TILE / 2, y + TILE / 2 + bounce);
          break;
        }
        case "U": {
          ctx.fillStyle = "#9c7b52";
          ctx.fillRect(x, y, TILE, TILE);
          ctx.strokeStyle = "#6b5236";
          ctx.lineWidth = 3;
          ctx.strokeRect(x + 1.5, y + 1.5, TILE - 3, TILE - 3);
          break;
        }
        case "S": {
          // 石ブロック(壊せない足場)。レンガと見分けがつくグレーにする
          ctx.fillStyle = "#a8a8b0";
          ctx.fillRect(x, y, TILE, TILE);
          ctx.strokeStyle = "#6e6e78";
          ctx.lineWidth = 3;
          ctx.strokeRect(x + 1.5, y + 1.5, TILE - 3, TILE - 3);
          ctx.fillStyle = "rgba(255,255,255,0.35)";
          ctx.fillRect(x + 5, y + 5, TILE - 10, 8);
          ctx.fillStyle = "rgba(0,0,0,0.15)";
          ctx.fillRect(x + 8, y + 26, 12, 8);
          ctx.fillRect(x + 28, y + 30, 10, 7);
          break;
        }
        case "C": {
          // ゴールの城(かざり・当たり判定なし)
          const gy = y + TILE; // 地面の上面
          const cw = TILE * 4, chh = TILE * 3.4;
          const cx0 = x - TILE * 1.5;
          ctx.fillStyle = "#d9d0c0";
          ctx.fillRect(cx0, gy - chh, cw, chh);
          // 城壁の凸凹
          ctx.fillStyle = "#c4b8a4";
          for (let i = 0; i < 5; i++) {
            ctx.fillRect(cx0 + i * (cw / 5) + 4, gy - chh - 14, cw / 5 - 8, 14);
          }
          // 窓とドア
          ctx.fillStyle = "#5b4636";
          ctx.beginPath();
          ctx.arc(cx0 + cw / 2, gy - 28, 22, Math.PI, 0);
          ctx.rect(cx0 + cw / 2 - 22, gy - 28, 44, 28);
          ctx.fill();
          ctx.fillRect(cx0 + 22, gy - chh + 24, 18, 24);
          ctx.fillRect(cx0 + cw - 40, gy - chh + 24, 18, 24);
          // てっぺんの旗
          ctx.fillStyle = "#e63946";
          ctx.fillRect(cx0 + cw / 2 - 2, gy - chh - 44, 4, 30);
          ctx.beginPath();
          ctx.moveTo(cx0 + cw / 2 + 2, gy - chh - 44);
          ctx.lineTo(cx0 + cw / 2 + 30, gy - chh - 36);
          ctx.lineTo(cx0 + cw / 2 + 2, gy - chh - 28);
          ctx.fill();
          break;
        }
        case "P": {
          const isTop = tileAt(tx, ty - 1) !== "P";
          const isLeft = tileAt(tx - 1, ty) !== "P";
          ctx.fillStyle = "#2ea44f";
          if (isTop) {
            // 土管の口(ふち)
            ctx.fillRect(x - (isLeft ? 4 : 0), y, TILE + 4, TILE);
            ctx.fillStyle = "rgba(255,255,255,0.25)";
            ctx.fillRect(x - (isLeft ? 4 : 0) + 4, y + 4, 10, TILE - 8);
            ctx.strokeStyle = "#1a7a35";
            ctx.lineWidth = 3;
            ctx.strokeRect(x - (isLeft ? 4 : 0) + 1.5, y + 1.5, TILE + 1, TILE - 3);
          } else {
            ctx.fillRect(x, y, TILE, TILE);
            ctx.fillStyle = "rgba(255,255,255,0.2)";
            ctx.fillRect(x + 6, y, 8, TILE);
            ctx.strokeStyle = "#1a7a35";
            ctx.lineWidth = 3;
            if (isLeft) { ctx.beginPath(); ctx.moveTo(x + 1.5, y); ctx.lineTo(x + 1.5, y + TILE); ctx.stroke(); }
            else { ctx.beginPath(); ctx.moveTo(x + TILE - 1.5, y); ctx.lineTo(x + TILE - 1.5, y + TILE); ctx.stroke(); }
          }
          break;
        }
        case "F": {
          const poleH = TILE * (MAP_ROWS - 3 - ty); // 地面まで届くポール
          const px = x + TILE / 2;
          // 土台
          ctx.fillStyle = "#a8a8b0";
          ctx.fillRect(x + 4, y + poleH - 14, TILE - 8, 14);
          ctx.strokeStyle = "#6e6e78";
          ctx.lineWidth = 3;
          ctx.strokeRect(x + 5.5, y + poleH - 12.5, TILE - 11, 11);
          // 旗ざお
          ctx.fillStyle = "#3fbf4f";
          ctx.fillRect(px - 4, y, 8, poleH - 12);
          ctx.beginPath();
          ctx.arc(px, y, 12, 0, Math.PI * 2);
          ctx.fillStyle = "#ffd166";
          ctx.fill();
          // 大きな旗
          const wave = Math.sin(frameCount * 0.08) * 5;
          ctx.fillStyle = "#e63946";
          ctx.beginPath();
          ctx.moveTo(px + 4, y + 14);
          ctx.lineTo(px + 72 + wave, y + 42);
          ctx.lineTo(px + 4, y + 70);
          ctx.closePath();
          ctx.fill();
          ctx.fillStyle = "#fff";
          ctx.font = "bold 20px sans-serif";
          ctx.textAlign = "left";
          ctx.textBaseline = "middle";
          ctx.fillText("GOAL", px + 10, y + 42);
          break;
        }
      }
    }
  }
}

function drawCoins() {
  for (const c of coins) {
    if (c.taken) continue;
    const squish = Math.abs(Math.sin(frameCount * 0.08 + c.x));
    ctx.save();
    ctx.translate(c.x, c.y + Math.sin(frameCount * 0.05 + c.x) * 3);
    ctx.scale(Math.max(0.15, squish), 1);
    ctx.fillStyle = "#ffd700";
    ctx.beginPath();
    ctx.arc(0, 0, 14, 0, Math.PI * 2);
    ctx.fill();
    ctx.strokeStyle = "#b8860b";
    ctx.lineWidth = 3;
    ctx.stroke();
    ctx.restore();
  }
}

// 恐竜のなかまを描く(アイテム時・騎乗時・逃走時で共用)
function drawDino(cx, feetY, facing, scale, runPhase) {
  ctx.save();
  ctx.translate(cx, feetY);
  if (facing < 0) ctx.scale(-1, 1);
  ctx.scale(scale, scale);
  ctx.fillStyle = "#3cb04a";
  // しっぽ
  ctx.beginPath();
  ctx.moveTo(-14, -18);
  ctx.quadraticCurveTo(-30, -16, -34, -5);
  ctx.quadraticCurveTo(-23, -9, -13, -8);
  ctx.fill();
  // 体
  ctx.beginPath();
  ctx.ellipse(-2, -14, 16, 13, 0, 0, Math.PI * 2);
  ctx.fill();
  // 足(ぱたぱた)
  const sw = Math.sin(runPhase) * 4;
  ctx.fillStyle = "#2d8a38";
  ctx.beginPath();
  ctx.ellipse(-8 + sw, -3, 6, 4.5, 0, 0, Math.PI * 2);
  ctx.ellipse(6 - sw, -3, 6, 4.5, 0, 0, Math.PI * 2);
  ctx.fill();
  // おなか
  ctx.fillStyle = "#eaf7d9";
  ctx.beginPath();
  ctx.ellipse(-1, -10, 10, 7, 0, 0, Math.PI * 2);
  ctx.fill();
  // 頭と鼻先
  ctx.fillStyle = "#3cb04a";
  ctx.beginPath();
  ctx.ellipse(13, -27, 11, 9, 0, 0, Math.PI * 2);
  ctx.ellipse(23, -24, 8, 6, 0, 0, Math.PI * 2);
  ctx.fill();
  // 目
  ctx.fillStyle = "#fff";
  ctx.beginPath();
  ctx.arc(13, -32, 4.5, 0, Math.PI * 2);
  ctx.fill();
  ctx.fillStyle = "#333";
  ctx.beginPath();
  ctx.arc(14.5, -32, 2, 0, Math.PI * 2);
  ctx.fill();
  // 背中のサドル
  ctx.fillStyle = "#e63946";
  ctx.beginPath();
  ctx.ellipse(-4, -25, 9, 5, 0, Math.PI, 0, true);
  ctx.fill();
  ctx.restore();
}

function drawItems() {
  for (const it of items) {
    if (it.taken) continue;
    const cx = it.x + it.w / 2;

    if (it.type === "buddy" || it.type === "flee") {
      drawDino(cx, it.y + it.h, it.vx >= 0 ? 1 : -1, 0.95, frameCount * 0.35);
      continue;
    }

    if (it.type === "star") {
      // ⭐ スター(くるくる回る)
      ctx.save();
      ctx.translate(cx, it.y + it.h / 2);
      ctx.rotate(Math.sin(frameCount * 0.15) * 0.3);
      ctx.fillStyle = "#ffd700";
      ctx.strokeStyle = "#c8960c";
      ctx.lineWidth = 3;
      ctx.beginPath();
      for (let i = 0; i < 10; i++) {
        const r = i % 2 === 0 ? 20 : 9;
        const a = (i * Math.PI) / 5 - Math.PI / 2;
        ctx.lineTo(Math.cos(a) * r, Math.sin(a) * r);
      }
      ctx.closePath();
      ctx.fill();
      ctx.stroke();
      ctx.fillStyle = "#333";
      ctx.beginPath();
      ctx.arc(-4, 2, 2.2, 0, Math.PI * 2);
      ctx.arc(4, 2, 2.2, 0, Math.PI * 2);
      ctx.fill();
      ctx.restore();
      continue;
    }

    if (it.type === "flower") {
      // 🌸 ファイアフラワー
      ctx.strokeStyle = "#2ea44f";
      ctx.lineWidth = 5;
      ctx.beginPath();
      ctx.moveTo(cx, it.y + it.h);
      ctx.lineTo(cx, it.y + 18);
      ctx.stroke();
      for (let i = 0; i < 6; i++) {
        const a = (i * Math.PI) / 3 + frameCount * 0.02;
        ctx.fillStyle = "#ff7043";
        ctx.beginPath();
        ctx.ellipse(cx + Math.cos(a) * 11, it.y + 14 + Math.sin(a) * 11, 7, 7, 0, 0, Math.PI * 2);
        ctx.fill();
      }
      ctx.fillStyle = "#ffd166";
      ctx.beginPath();
      ctx.arc(cx, it.y + 14, 8, 0, Math.PI * 2);
      ctx.fill();
      ctx.fillStyle = "#333";
      ctx.beginPath();
      ctx.arc(cx - 3, it.y + 13, 1.8, 0, Math.PI * 2);
      ctx.arc(cx + 3, it.y + 13, 1.8, 0, Math.PI * 2);
      ctx.fill();
      continue;
    }
    // 軸(顔の部分)
    ctx.fillStyle = "#ffe8c9";
    ctx.beginPath();
    ctx.roundRect(it.x + 7, it.y + it.h * 0.45, it.w - 14, it.h * 0.5, 6);
    ctx.fill();
    // 目
    ctx.fillStyle = "#333";
    ctx.beginPath();
    ctx.arc(cx - 6, it.y + it.h * 0.68, 2.5, 0, Math.PI * 2);
    ctx.arc(cx + 6, it.y + it.h * 0.68, 2.5, 0, Math.PI * 2);
    ctx.fill();
    // カサ
    ctx.fillStyle = "#e63946";
    ctx.beginPath();
    ctx.arc(cx, it.y + it.h * 0.5, it.w / 2, Math.PI, 0);
    ctx.closePath();
    ctx.fill();
    // カサの白い模様
    ctx.fillStyle = "#fff";
    ctx.beginPath();
    ctx.arc(cx, it.y + it.h * 0.16, 5, 0, Math.PI * 2);
    ctx.arc(cx - 12, it.y + it.h * 0.38, 4, 0, Math.PI * 2);
    ctx.arc(cx + 12, it.y + it.h * 0.38, 4, 0, Math.PI * 2);
    ctx.fill();
  }
}

